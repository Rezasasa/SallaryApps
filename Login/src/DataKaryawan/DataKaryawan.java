    public void cekNik(){
        String input = JOptionPane.showInputDialog("Masukan NIK yang ingin diedit :");
    
        try {
            String sql = "select * from tablekaryawan where NIK="+input;
            java.sql.Connection conn=(Connection)Koneksi.getKoneksiKry();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);

            if (res == null){
                JOptionPane.showMessageDialog(rootPane, "NIK Tidak ada");
            }else{
                if(res.next()){
                    String nik = res.getString("NIK");
                    nikTxt.setText(nik);
                    String nama = res.getString("Nama");
                    namaTxt.setText(nama);
                    String tanggal = res.getString("Tanggal_Lahir");
                    tanggalTxt.setText(tanggal);
                    String alamat = res.getString("Alamat");
                    alamatTxt.setText(alamat);
                    String no = res.getString("No_Telepon");
                    telTxt.setText(no);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
     
    }
    
