    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (nikTxt.getText().isEmpty() || namaTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama atau NIK belum lengkap",
                "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else if(tanggalTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tanggal Lahir belum diisi",
                "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else if(genderTxt.isEditable()== true){
            JOptionPane.showMessageDialog(null, "Jenis kelamin belum dipilih",
                "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else if(alamatTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Alamat belum diisi",
                "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else if (telTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No.Telpon belum diisi",
                "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
        create();
        
        nikTxt.setText("");
        namaTxt.setText("");
        tanggalTxt.setText("DD-MM-YYYY");
        genderTxt.setSelectedItem(this);
        alamatTxt.setText("");
        telTxt.setText("");
        golTxt.setSelectedItem(this);
        
        getData();
    }//GEN-LAST:event_jButton3ActionPerformed

